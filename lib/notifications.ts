/**
 * Returns true when running inside a Capacitor native shell (iOS/Android).
 * Safe to call server-side (returns false in SSR/Node context).
 * Used as the branch point for the native LocalNotifications upgrade path.
 */
export function isCapacitorNative(): boolean {
  if (typeof window === 'undefined') return false;
  // window.Capacitor is injected by the Capacitor runtime on native platforms.
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  return !!(window as any).Capacitor?.isNativePlatform?.();
}

