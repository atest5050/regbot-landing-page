/**
 * v56 — Try to fire a native notification via @capacitor/local-notifications.
 * Uses dynamic require() so the call is safe even if the package is NOT installed:
 * the catch block returns false and the caller falls through to the browser API.
 *
 * @returns true if the notification was dispatched via the native Capacitor plugin,
 *          false if the package is absent or the call failed (caller should fallback).
 *
 * Never throws — all errors are swallowed and returned as false.
 */
export async function tryCapacitorLocalNotification(payload: {
  title:     string;
  body:      string;
  channelId?: string;
}): Promise<boolean> {
  if (!isCapacitorNative()) return false;
  try {
    // v57: dynamic import with webpackIgnore so Turbopack/webpack skip static resolution.
    // Resolves only at runtime when @capacitor/local-notifications is installed.
    // If missing, the import() rejects → caught below → returns false.
    // @ts-ignore — module is an optional native peer-dep; type errors are suppressed intentionally.
    // eslint-disable-next-line @typescript-eslint/ban-ts-comment
    const { LocalNotifications } = await import(/* webpackIgnore: true */ '@capacitor/local-notifications') as {
      LocalNotifications: {
        schedule: (opts: {
          notifications: Array<{
            title: string; body: string; id: number; channelId?: string;
          }>;
        }) => Promise<void>;
      };
    };
    // Notification IDs must be unique 32-bit signed integers.
    const id = Math.floor(Date.now() / 1000) % 2_147_483_647;
    await LocalNotifications.schedule({
      notifications: [{
        title:     payload.title,
        body:      payload.body,
        id,
        channelId: payload.channelId ?? 'regpulse-alerts',
      }],
    });
    return true;
  } catch {
    // Package not installed, permission denied, or schedule threw — fall through.
    return false;
  }
}

