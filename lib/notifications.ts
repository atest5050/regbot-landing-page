// ── Permission helpers ─────────────────────────────────────────────────────────

/**
 * Get the current notification permission without prompting the user.
 * Returns "unsupported" when the Notification API is unavailable
 * (e.g. non-secure context, very old browser, restrictive WebView).
 */
export function getNotifPermission(): NotificationPermission | "unsupported" {
  if (typeof Notification === "undefined") return "unsupported";
  return Notification.permission;
}

/**
 * Request notification permission. Shows the browser prompt if permission is "default".
 * Returns the resulting permission state, or "unsupported" if Notification API is absent.
 * Never throws — all errors are caught and returned as "denied".
 */
export async function requestNotifPermission(): Promise<NotificationPermission | "unsupported"> {
  if (typeof Notification === "undefined") return "unsupported";
  try {
    return await Notification.requestPermission();
  } catch {
    // requestPermission() can throw in restricted contexts (cross-origin iframes, etc.)
    return "denied";
  }
}

// ── Notification firing ────────────────────────────────────────────────────────

/**
 * Fire (or schedule) a browser notification.
 * - delayMs ≤ 0: fires immediately
 * - delayMs > 0: fires after the given delay via setTimeout (session-scoped)
 *
 * No-ops when:
 *   • Notification API unavailable
 *   • Permission is not "granted"
 * Never throws.
 */
export function scheduleNotification(opts: NotifOptions, delayMs = 0): void {
  if (typeof Notification === "undefined") return;
  if (Notification.permission !== "granted") return;

  const fire = () => {
    try {
      new Notification(opts.title, {
        body: opts.body,
        tag:  opts.tag,
        icon: opts.icon ?? "/icon-192x192.png",
      });
    } catch {
      // Notification constructor can throw in some restricted WebViews — ignore silently
    }
  };

  if (delayMs <= 0) {
    fire();
  } else {
    setTimeout(fire, delayMs);
  }
}

// ── Domain-specific helpers ────────────────────────────────────────────────────

/**
 * Schedule a renewal reminder notification for a single checklist item.
 * Fires 1 day before the due date (or immediately for "due today").
 *
 * @param formName   Human-readable form name (e.g. "Business License")
 * @param bizName    Business name (e.g. "Joe's Tacos")
 * @param daysLeft   Days until renewal (negative = overdue, 0 = today)
 */
export function scheduleRenewalNotification(
  formName: string,
  bizName: string,
  daysLeft: number,
): void {
  // For overdue items, fire immediately so the user is informed now.
  // For future items, fire 1 day before (floor at 0 so "1d" → immediate reminder).
  const delayMs = daysLeft <= 1 ? 0 : (daysLeft - 1) * 24 * 60 * 60 * 1000;

  scheduleNotification(
    {
      title: daysLeft <= 0
        ? `Overdue renewal: ${formName}`
        : `Renewal due${daysLeft === 1 ? " tomorrow" : ` in ${daysLeft} days`}: ${formName}`,
      body:  `${bizName} — ${daysLeft <= 0 ? `${Math.abs(daysLeft)} days overdue` : `due ${daysLeft === 0 ? "today" : `in ${daysLeft} days`}`}. Tap to renew.`,
      tag:   `renewal-${formName.toLowerCase().replace(/\W+/g, "-")}-${bizName.toLowerCase().replace(/\W+/g, "-")}`,
    },
    delayMs,
  );
}

/**
 * v56 — Fire an immediate notification when a new rule-change alert is generated for a business.
 * NOW ASYNC: tries the native Capacitor LocalNotifications path first (when installed + native),
 * then falls back to the browser Notification API for web/PWA users.
 *
 * Callers use:  void fireRuleAlertNotification(bizName, title, desc)
 *          or:  await fireRuleAlertNotification(bizName, title, desc)
 * Both forms are safe — the returned Promise<void> is always resolved (never rejects).
 *
 * Native path (Capacitor, @capacitor/local-notifications installed):
 *   → tryCapacitorLocalNotification() → LocalNotifications.schedule() → real tray notification.
 * Web/PWA fallback:
 *   → scheduleNotification() → browser Notification API.
 *
 * @param bizName          Business name
 * @param alertTitle       Short alert title
 * @param alertDescription Full description (auto-truncated to 120 chars)
 */
export async function fireRuleAlertNotification(
  bizName: string,
  alertTitle: string,
  alertDescription: string,
): Promise<void> {
  const bodySnippet =
    alertDescription.length > 120
      ? alertDescription.slice(0, 117) + "…"
      : alertDescription;

  const title = `RegPulse Alert — ${bizName}`;
  const body  = `${alertTitle}: ${bodySnippet}`;

  // v56 — Try native Capacitor dispatch first.
  if (isCapacitorNative()) {
    const sent = await tryCapacitorLocalNotification({ title, body });
    if (sent) return; // native notification delivered — do not also fire browser API
  }

  // Web/PWA fallback (also covers Capacitor WebView when plugin is absent).
  scheduleNotification({
    title,
    body,
    tag: `rule-alert-${bizName.toLowerCase().replace(/\W+/g, "-")}-${Date.now()}`,
  });
}

/**
 * Schedule renewal notifications for all items due within the given day-window.
 * Safe to call on component mount — skips items if permission is not "granted".
 *
 * @param renewals  Array of { formName, bizName, daysLeft } objects
 * @param maxDays   Only schedule for items due within this many days (default 7)
 */
export function scheduleUpcomingRenewalNotifications(
  renewals: Array<{ formName: string; bizName: string; daysLeft: number }>,
  maxDays = 7,
): void {
  for (const r of renewals) {
    if (r.daysLeft <= maxDays) {
      scheduleRenewalNotification(r.formName, r.bizName, r.daysLeft);
    }
  }
}

// ── v31: Portfolio digest notification ────────────────────────────────────────

/**
 * v31/v56 — Fire a single digest notification when multiple businesses in the portfolio
 * have active rule-change alerts. Reduces notification fatigue compared to firing
 * one notification per business alert when the portfolio is large.
 *
 * v56 UPGRADE: NOW ASYNC — tries native Capacitor dispatch first, browser API fallback.
 *
 * Only fires when alertCount ≥ 2 (single-alert case is handled by fireRuleAlertNotification).
 * Safe to call on alert generation — no-ops when permission is not "granted".
 *
 * @param alertCount       Total number of active, undismissed alerts across all businesses
 * @param bizNames         Array of business names with active alerts (used in body text)
 */
export async function schedulePortfolioDigestNotification(
  alertCount: number,
  bizNames: string[],
): Promise<void> {
  if (alertCount < 2) return; // single-alert case handled by fireRuleAlertNotification

  const listed = bizNames.slice(0, 3).join(", ");
  const more   = bizNames.length > 3 ? ` + ${bizNames.length - 3} more` : "";

  const title = `RegPulse — ${alertCount} Compliance Alerts`;
  const body  = `${listed}${more} have new rule changes. Tap to review your portfolio.`;

  // v56 — Try native Capacitor dispatch first.
  if (isCapacitorNative()) {
    const sent = await tryCapacitorLocalNotification({ title, body });
    if (sent) return;
  }

  scheduleNotification({
    title,
    body,
    tag: `portfolio-digest-${Date.now()}`,
  });
}
